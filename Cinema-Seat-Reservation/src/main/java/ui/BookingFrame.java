    private void loadShowTimes(int movieId){

    cmbShowTime.removeAllItems();
    cmbSeat.removeAllItems();

    cmbShowTime.setEnabled(true);
    cmbSeat.setEnabled(false);

    showTimesById.clear();
    seatsById.clear();

    ShowTimeDAO dao =
            new ShowTimeDAO();

    for(ShowTime s :
            dao.getShowTimesByMovie(movieId)){

        showTimesById.put(s.getShowtimeId(), s);

        cmbShowTime.addItem(

                s.getShowtimeId()
                + " | "
                + s.getShowDate()
                + " "
                + s.getShowTime()
        );
    }
    
    if(cmbShowTime.getItemCount() == 0){

        JOptionPane.showMessageDialog(
                this,
                "No showtimes available for this movie.");

        cmbShowTime.setEnabled(false);
        cmbSeat.setEnabled(false);
    }

    if(cmbShowTime.getItemCount() > 0){
        cmbShowTime.setSelectedIndex(0);
        loadSeatsForSelectedShowTime();
    }
}
    
        private void loadMovies() {

        DefaultTableModel model =
                (DefaultTableModel) tblMovies.getModel();

        model.setRowCount(0);

        MovieDAO dao = new MovieDAO();

        for(Movie movie : dao.getAllMovies()) {

            model.addRow(new Object[]{

                movie.getMovieId(),
                movie.getTitle(),
                movie.getGenre(),
                movie.getDurationMinutes(),
                movie.getRating()
            });
        }
    }
    
