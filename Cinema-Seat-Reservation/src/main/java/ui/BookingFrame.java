    private void btnBookActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnBookActionPerformed

    if(tblMovies.getSelectedRow() == -1){
        JOptionPane.showMessageDialog(
                this,
                "Please select a movie first.");
        return;
    }

    if(cmbShowTime.getSelectedItem() == null){
        JOptionPane.showMessageDialog(
                this,
                "Please select a show time.");
        return;
    }

    if(cmbSeat.getSelectedItem() == null){
        JOptionPane.showMessageDialog(
                this,
                "Please select a seat.");
        return;
    }

    int userId = UserSession.getCurrentUserId();

    if(userId <= 0){

        JOptionPane.showMessageDialog(
                this,
                "Please login before making a booking.");

        new LoginFrame().setVisible(true);
        dispose();

        return;
    }

    int row = tblMovies.getSelectedRow();

    String movie =
            tblMovies.getValueAt(row, 1).toString();

    String showTime =
            cmbShowTime.getSelectedItem().toString();

    String seatText =
            cmbSeat.getSelectedItem().toString();

    int showtimeId =
            getSelectedId(cmbShowTime.getSelectedItem());

    int seatId =
            getSelectedId(cmbSeat.getSelectedItem());

    Seat seat =
            seatsById.get(seatId);

    double amount =
            calculatePrice(seat);

    PaymentFrame pf =
            new PaymentFrame(
                    userId,
                    showtimeId,
                    seatId,
                    movie,
                    showTime,
                    seatText,
                    amount);

    pf.setVisible(true);

    this.dispose();
        
// TODO add your handling code here:
    }//GEN-LAST:event_btnBookActionPerformed

