    private void loadSeatsForSelectedShowTime(){
        int showtimeId = getSelectedId(cmbShowTime.getSelectedItem());
        ShowTime showTime = showTimesById.get(showtimeId);

        cmbSeat.removeAllItems();
        seatsById.clear();

        if(showTime == null){
            return;
        }

        SeatDAO seatDAO = new SeatDAO();
        List<Seat> seats = seatDAO.getAvailableSeats(
                showTime.getShowtimeId(),
                showTime.getHallNo());

        for(Seat seat : seats){
            seatsById.put(seat.getSeatId(), seat);
            cmbSeat.addItem(
                    seat.getSeatId()
                    + " | "
                    + seat.getSeatRow()
                    + seat.getSeatNumber()
                    + " | "
                    + seat.getSeatType()
                    + " | Rs. "
                    + String.format("%.2f", calculatePrice(seat)));
        }
        
        if(cmbSeat.getItemCount() == 0){

            JOptionPane.showMessageDialog(
                    this,
                    "No seats available for this showtime.");

            cmbSeat.setEnabled(false);
        }
    }

    private int getSelectedId(Object selectedItem){
        if(selectedItem == null){
            return 0;
        }

        String value = selectedItem.toString();
        int separator = value.indexOf('|');
        String idText = separator >= 0
                ? value.substring(0, separator).trim()
                : value.trim();

        try{
            return Integer.parseInt(idText);
        }catch(NumberFormatException e){
            return 0;
        }
    }

    private double calculatePrice(Seat seat){
        if(seat == null || seat.getSeatType() == null){
            return 1000.00;
        }

        return switch(seat.getSeatType().toUpperCase()){
            case "VIP" -> 2000.00;
            case "PREMIUM" -> 1500.00;
            default -> 1000.00;
        };
    }

