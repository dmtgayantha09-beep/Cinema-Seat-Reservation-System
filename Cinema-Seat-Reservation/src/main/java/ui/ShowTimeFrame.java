    private void btnAddShowTimeActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnAddShowTimeActionPerformed
        try{

            ShowTime showTime =
                    new ShowTime();

            showTime.setMovieId(
                    Integer.parseInt(
                            txtID.getText()));

            LocalDate.parse(
                    txtDate.getText());

            showTime.setShowDate(
                    txtDate.getText());

            LocalTime.parse(
                    txtTime.getText());

            showTime.setShowTime(
                    txtTime.getText());

            showTime.setHallNo(
                    Integer.parseInt(
                            txtHollNo.getText()));

            ShowTimeDAO dao =
                    new ShowTimeDAO();

            if(dao.addShowTime(showTime)){

                JOptionPane.showMessageDialog(
                        this,
                        "Show Time Added");

                loadShowTimes();

                txtID.setText("");
                txtDate.setText("");
                txtTime.setText("");
                txtHollNo.setText("");

            }else{

                JOptionPane.showMessageDialog(
                        this,
                        "Failed");
            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(
                    this,
                    "Invalid Date or Time Format\n\n"
                    + "Date : YYYY-MM-DD\n"
                    + "Time : HH:MM:SS");
        }

// TODO add your handling code here:
    }//GEN-LAST:event_btnAddShowTimeActionPerformed

