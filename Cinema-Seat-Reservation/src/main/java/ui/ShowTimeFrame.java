        private void loadShowTimes(){

        DefaultTableModel model =
                (DefaultTableModel)
                        tblShowTimes.getModel();

        model.setRowCount(0);

        ShowTimeDAO dao =
                new ShowTimeDAO();

        for(ShowTime s :
                dao.getAllShowTimes()){

            model.addRow(
            new Object[]{

                    s.getMovieTitle(),
                    s.getShowDate(),
                    s.getShowTime(),
                    s.getHallNo()
            });
        }
    }
    
