    private void btnCancelBookingActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnCancelBookingActionPerformed
        
        int row =
            tblBookings.getSelectedRow();

    if(row == -1){

        JOptionPane.showMessageDialog(
                this,
                "Select a booking");

        return;
    }

    int bookingId =
            Integer.parseInt(
                    tblBookings.getValueAt(
                            row,
                            0).toString());

    String status =
            tblBookings.getValueAt(
                    row,
                    6).toString();

    if(status.equalsIgnoreCase(
            Booking.STATUS_CANCELLED)){

        JOptionPane.showMessageDialog(
                this,
                "Already Cancelled");

        return;
    }
    
    BookingDAO dao =
            new BookingDAO();

    if(dao.cancelBooking(bookingId)){

        JOptionPane.showMessageDialog(
                this,
                "Booking Cancelled");

        loadBookings();

    }else{

        JOptionPane.showMessageDialog(
                this,
                "Cancel Failed");
    }
        // TODO add your handling code here:
    }//GEN-LAST:event_btnCancelBookingActionPerformed

