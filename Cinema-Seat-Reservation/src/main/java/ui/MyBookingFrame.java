    private void loadBookings(){

    DefaultTableModel model =
            (DefaultTableModel)
                    tblBookings.getModel();

    model.setRowCount(0);

    BookingDAO dao =
            new BookingDAO();

        try{

            List<Booking> bookings =
                    dao.getBookingsByUser(
                            UserSession.getCurrentUserId());

            for(Booking booking : bookings){

                model.addRow(
                        new Object[]{

                                booking.getBookingId(),

                                booking.getMovieTitle(),

                                booking.getShowDate(),

                                booking.getShowTime(),

                                booking.getSeat(),

                                booking.getTotalPrice(),

                                booking.getStatus()
                        });
            }

        }catch(Exception e){

            JOptionPane.showMessageDialog(
                    this,
                    e.getMessage());
        }
    }
    
